export const getServerSideProps: GetServerSideProps = async () => {
  try {
    const users = await prisma.user.findMany({
      include: { profile: true }
    });

    const boards = await prisma.board.findMany({
      include: { columns: true }
    });

    return {
      props: { users, boards }
    };
  } catch (error) {
    console.error(error);
    throw error;
  }
};

